package egovframework.let.sym.grp.service;

public class GroupInfo {

	private String groupCode;
	private String groupNm;
	private String groupUseYn;
	
	private String menuGubun;	
	
	private String mode;
	
	
	public String getMenuGubun() {
		return menuGubun;
	}
	public void setMenuGubun(String menuGubun) {
		this.menuGubun = menuGubun;
	}
	public String getGroupUseYn() {
		return groupUseYn;
	}
	public void setGroupUseYn(String groupUseYn) {
		this.groupUseYn = groupUseYn;
	}
	
	
	
	
	
	
	public String getMode() {
		return mode;
	}
	public void setMode(String mode) {
		this.mode = mode;
	}
	public String getGroupCode() {
		return groupCode;
	}
	public void setGroupCode(String groupCode) {
		this.groupCode = groupCode;
	}
	public String getGroupNm() {
		return groupNm;
	}
	public void setGroupNm(String groupNm) {
		this.groupNm = groupNm;
	}
	
	
	
}
