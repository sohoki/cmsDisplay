package egovframework.let.sym.grp.service;

public class GroupDidInfo {

	private String groupCode;
	private String didId;
	private String didNm;
	private String mode;
	
	private String menuGubun;	
	
	
	
	public String getMenuGubun() {
		return menuGubun;
	}
	public void setMenuGubun(String menuGubun) {
		this.menuGubun = menuGubun;
	}
	public String getMode() {
		return mode;
	}
	public void setMode(String mode) {
		this.mode = mode;
	}
	public String getGroupCode() {
		return groupCode;
	}
	public void setGroupCode(String groupCode) {
		this.groupCode = groupCode;
	}
	
	public String getDidId() {
		return didId;
	}
	public void setDidId(String didId) {
		this.didId = didId;
	}
	public String getDidNm() {
		return didNm;
	}
	public void setDidNm(String didNm) {
		this.didNm = didNm;
	}
	
	
	
	
}
