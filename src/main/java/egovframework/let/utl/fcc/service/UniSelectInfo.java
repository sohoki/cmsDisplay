public class UniSelectInfo {

	private String inResult;
	private String inTable;
	private String inColumn;
	private String inValue;
	private String vResult;
	
	
	public String getInResult() {
		return inResult;
	}
	public void setInResult(String inResult) {
		this.inResult = inResult;
	}
	public String getInTable() {
		return inTable;
	}
	public void setInTable(String inTable) {
		this.inTable = inTable;
	}
	public String getInColumn() {
		return inColumn;
	}
	public void setInColumn(String inColumn) {
		this.inColumn = inColumn;
	}
	public String getInValue() {
		return inValue;
	}
	public void setInValue(String inValue) {
		this.inValue = inValue;
	}
	public String getvResult() {
		return vResult;
	}
	public void setvResult(String vResult) {
		this.vResult = vResult;
	}
	
	
	
	
	
}
